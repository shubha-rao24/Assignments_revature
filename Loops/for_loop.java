package Loops;
//for loop exaple generating even n odd numbers 1 tp 30
public class for_loop {

	public static void main(String[] args)
	{
				
	
	     for(int x=2;x<=30;x++)
	     {
	    	 if(x%2==0)
	    	 {
	    		 System.out.print(x);
	    		 System.out.println(" is even");
	    	 }
	    	 
	    	 else
	    	 {
	    		 System.out.print(x);
	    		 System.out.println("is odd");
	    	 }
	     }
	        
	       
	   }

}
